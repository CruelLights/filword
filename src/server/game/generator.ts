const DIRECTIONS_LIST: Direction[] = [
  "horizontal", "vertical", "diagonal_down", "diagonal_up",
];

export function generateGrid(size: number = 10): {
  grid: string[][];
  placements: WordPlacement[];
} {
  const grid: string[][] = Array.from({ length: size }, () => Array(size).fill(""));
  const placements: WordPlacement[] = [];

  const suitable = WORD_DICTIONARY
    .filter((w) => w.length >= 3 && w.length <= size)
    .sort(() => Math.random() - 0.5);

  for (const word of suitable) {
    if (placements.length >= 15) break;
    const placed = tryPlaceWord(grid, word, size);
    if (placed) placements.push(placed);
  }

  for (let r = 0; r < size; r++) {
    for (let c = 0; c < size; c++) {
      if (!grid[r]![c]) {
        grid[r]![c] = RU_LETTERS[Math.floor(Math.random() * RU_LETTERS.length)]!;
      }
    }
  }

  return { grid, placements };
}

function tryPlaceWord(grid: string[][], word: string, size: number): WordPlacement | null {
  const directions = [...DIRECTIONS_LIST].sort(() => Math.random() - 0.5);

  for (const direction of directions) {
    const { dr, dc } = DIRECTIONS[direction];
    const maxRow = dr >= 0 ? size - dr * (word.length - 1) : size - 1;
    const minRow = dr < 0 ? -dr * (word.length - 1) : 0;
    const maxCol = dc >= 0 ? size - dc * (word.length - 1) : size - 1;
    const minCol = dc < 0 ? -dc * (word.length - 1) : 0;

    if (maxRow < minRow || maxCol < minCol) continue;

    for (let attempt = 0; attempt < 40; attempt++) {
      const startRow = minRow + Math.floor(Math.random() * (maxRow - minRow + 1));
      const startCol = minCol + Math.floor(Math.random() * (maxCol - minCol + 1));

      if (canPlaceWord(grid, word, startRow, startCol, direction, size)) {
        const cells = getWordCells(startRow, startCol, direction, word.length);
        for (let i = 0; i < cells.length; i++) {
          grid[cells[i]!.row]![cells[i]!.col] = word[i]!;
        }
        return { word, startRow, startCol, direction, length: word.length };
      }
    }
  }
  return null;
}

function canPlaceWord(
  grid: string[][], word: string,
  startRow: number, startCol: number,
  direction: Direction, size: number
): boolean {
  const cells = getWordCells(startRow, startCol, direction, word.length);
  for (let i = 0; i < cells.length; i++) {
    const { row, col } = cells[i]!;
    if (row < 0 || row >= size || col < 0 || col >= size) return false;
    const existing = grid[row]![col];
    // Запрещаем любое наложение на уже занятые клетки
    if (existing) return false;
  }
  return true;
}
